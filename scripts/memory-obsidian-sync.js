#!/usr/bin/env node
/**
 * memory-obsidian-sync.js
 * Syncs Claude memory files → Obsidian "Agentic knowledge" vault
 * Run: node memory-obsidian-sync.js
 * Heartbeat: call this from HEARTBEAT.md check or cron
 */

import { readdir, readFile, writeFile, copyFile, mkdir } from 'fs/promises';
import { existsSync } from 'fs';
import { resolve, basename, join } from 'path';
import { homedir } from 'os';

const CLAUDE_MEMORY = resolve(homedir(), '.claude/projects/C--Users-Rohan/memory');
const OBSIDIAN_VAULT = resolve(homedir(), 'OneDrive/Documents/Agentic knowledge');
const OBSIDIAN_MEMORY = join(OBSIDIAN_VAULT, 'Memory');

const today = new Date().toISOString().slice(0, 10);

async function ensureDir(dir) {
  if (!existsSync(dir)) await mkdir(dir, { recursive: true });
}

async function syncMemoryFiles() {
  await ensureDir(OBSIDIAN_MEMORY);
  
  const files = await readdir(CLAUDE_MEMORY);
  const mdFiles = files.filter(f => f.endsWith('.md') && !f.startsWith('.'));
  
  let synced = 0;
  const links = [];

  for (const file of mdFiles) {
    const src = join(CLAUDE_MEMORY, file);
    const dst = join(OBSIDIAN_MEMORY, file);
    await copyFile(src, dst);
    synced++;
    const name = file.replace('.md', '');
    links.push(`- [[Memory/${name}]]`);
  }

  console.log(`✓ Synced ${synced} memory files`);
  return links;
}

async function getAeonActivity() {
  try {
    const { execSync } = await import('child_process');
    const result = execSync(
      'gh run list --repo bludragon66613-sys/NERV_02 --limit 5 --json displayTitle,status,conclusion,createdAt',
      { encoding: 'utf8', timeout: 10000 }
    );
    const runs = JSON.parse(result);
    if (!runs.length) return ['- No Aeon activity today'];
    return runs.map(r => `- ${r.displayTitle} — ${r.status}/${r.conclusion || 'pending'} (${r.createdAt.slice(0, 10)})`);
  } catch {
    return ['- Aeon activity check failed (gh CLI unavailable)'];
  }
}

async function writeDailyNote(links, aeonLines) {
  const notePath = join(OBSIDIAN_VAULT, `${today}.md`);
  
  // Don't overwrite if already exists with content
  if (existsSync(notePath)) {
    const existing = await readFile(notePath, 'utf8');
    if (existing.length > 100 && !existing.includes('Auto-generated')) {
      console.log(`✓ Daily note ${today}.md already exists, skipping overwrite`);
      return;
    }
  }

  const content = `# ${today} - Daily Sync

> Auto-generated by memory-obsidian-sync.js

## Active Services
- OpenClaw: \`openclaw status\`
- Paperclip: http://localhost:3100
- Dashboard: http://localhost:5555

## Memory Files
${links.join('\n')}

## Aeon Logs
${aeonLines.join('\n')}
`;

  await writeFile(notePath, content, 'utf8');
  console.log(`✓ Daily note written: ${today}.md`);
}

async function updateMindMap(links) {
  const mmPath = join(OBSIDIAN_VAULT, 'MindMap.md');
  
  const sessions = [];
  try {
    const sessDir = join(OBSIDIAN_VAULT, 'Claude Sessions');
    const sessFiles = await readdir(sessDir);
    sessions.push(...sessFiles.filter(f => f.endsWith('.tmp') || f.endsWith('.md'))
      .sort().reverse().slice(0, 10)
      .map(f => `- [[Claude Sessions/${f.replace(/\.(tmp|md)$/, '')}]]`));
  } catch { /* no sessions dir */ }

  const content = `# MindMap - Agent Memory Graph
> Auto-generated by memory-obsidian-sync.js | Last sync: ${new Date().toISOString()}

## Memory Files (${links.length})
${links.join('\n')}

## Recent Claude Sessions
${sessions.length ? sessions.join('\n') : '- No sessions found'}

## Vaults
- [[Agentic knowledge]] (OneDrive) — primary memory mirror
- knowledge-base (local) — raw sources + wiki
`;

  await writeFile(mmPath, content, 'utf8');
  console.log(`✓ MindMap.md updated`);
}

async function main() {
  console.log(`\n=== memory-obsidian-sync.js | ${today} ===`);
  
  try {
    const [links, aeonLines] = await Promise.all([
      syncMemoryFiles(),
      getAeonActivity()
    ]);
    
    await writeDailyNote(links, aeonLines);
    await updateMindMap(links);
    
    console.log('\n✅ Sync complete');
  } catch (err) {
    console.error('❌ Sync failed:', err.message);
    process.exit(1);
  }
}

main();
