#!/usr/bin/env node
/**
 * memory-obsidian-sync.mjs
 * Syncs Claude memory files → Obsidian "Agentic knowledge" vault
 */

import { readdir, readFile, writeFile, copyFile, mkdir } from 'fs/promises';
import { existsSync } from 'fs';
import { resolve, join } from 'path';
import { homedir } from 'os';
import { execSync } from 'child_process';

const CLAUDE_MEMORY = resolve(homedir(), '.claude/projects/C--Users-Rohan/memory');
const OBSIDIAN_VAULT = resolve(homedir(), 'OneDrive/Documents/Agentic knowledge');
const OBSIDIAN_MEMORY = join(OBSIDIAN_VAULT, 'Memory');

const today = new Date().toISOString().slice(0, 10);

async function ensureDir(dir) {
  if (!existsSync(dir)) await mkdir(dir, { recursive: true });
}

async function syncMemoryFiles() {
  await ensureDir(OBSIDIAN_MEMORY);
  const files = await readdir(CLAUDE_MEMORY);
  const mdFiles = files.filter(f => f.endsWith('.md') && !f.startsWith('.'));
  const links = [];
  for (const file of mdFiles) {
    await copyFile(join(CLAUDE_MEMORY, file), join(OBSIDIAN_MEMORY, file));
    links.push(`- [[Memory/${file.replace('.md', '')}]]`);
  }
  console.log(`✓ Synced ${mdFiles.length} memory files`);
  return links;
}

function getAeonActivity() {
  try {
    const result = execSync(
      'gh run list --repo bludragon66613-sys/NERV_02 --limit 5 --json displayTitle,status,conclusion,createdAt',
      { encoding: 'utf8', timeout: 10000 }
    );
    const runs = JSON.parse(result);
    if (!runs.length) return ['- No Aeon activity today'];
    return runs.map(r => `- ${r.displayTitle} — ${r.status}/${r.conclusion || 'pending'} (${r.createdAt.slice(0, 10)})`);
  } catch {
    return ['- Aeon activity check skipped'];
  }
}

async function writeDailyNote(links, aeonLines) {
  const notePath = join(OBSIDIAN_VAULT, `${today}.md`);
  if (existsSync(notePath)) {
    const existing = await readFile(notePath, 'utf8');
    if (existing.length > 100 && !existing.includes('Auto-generated')) {
      console.log(`✓ Daily note ${today}.md exists, keeping`);
      return;
    }
  }
  const content = `# ${today} - Daily Sync\n\n> Auto-generated by memory-obsidian-sync.mjs\n\n## Active Services\n- OpenClaw: \`openclaw status\`\n- Paperclip: http://localhost:3100\n- Dashboard: http://localhost:5555\n\n## Memory Files\n${links.join('\n')}\n\n## Aeon Logs\n${aeonLines.join('\n')}\n`;
  await writeFile(notePath, content, 'utf8');
  console.log(`✓ Daily note: ${today}.md`);
}

async function updateMindMap(links) {
  const mmPath = join(OBSIDIAN_VAULT, 'MindMap.md');
  let sessions = [];
  try {
    const sessFiles = await readdir(join(OBSIDIAN_VAULT, 'Claude Sessions'));
    sessions = sessFiles.filter(f => f.endsWith('.tmp') || f.endsWith('.md'))
      .sort().reverse().slice(0, 10)
      .map(f => `- [[Claude Sessions/${f.replace(/\.(tmp|md)$/, '')}]]`);
  } catch { /* no sessions */ }

  const content = `# MindMap - Agent Memory Graph\n> Last sync: ${new Date().toISOString()}\n\n## Memory Files (${links.length})\n${links.join('\n')}\n\n## Recent Claude Sessions\n${sessions.length ? sessions.join('\n') : '- None'}\n`;
  await writeFile(mmPath, content, 'utf8');
  console.log(`✓ MindMap.md updated`);
}

console.log(`\n=== memory-obsidian-sync | ${today} ===`);
const links = await syncMemoryFiles();
const aeonLines = getAeonActivity();
await writeDailyNote(links, aeonLines);
await updateMindMap(links);

// Sync Spike's workspace MEMORY.md as SPIKE_MEMORY.md
try {
  const spikeMem = resolve(homedir(), '.openclaw/workspace/MEMORY.md');
  if (existsSync(spikeMem)) {
    await copyFile(spikeMem, join(OBSIDIAN_MEMORY, 'SPIKE_MEMORY.md'));
    console.log('✓ SPIKE_MEMORY.md synced');
  }
} catch { /* not critical */ }

console.log('✅ Done');;

